# 幂运算（指数运算）示例

print(2 ** 2)  # 2的2次方 = 2 × 2 = 4
print(2 ** 3)  # 2的3次方 = 2 × 2 × 2 = 8

print('-' * 50)

# 方法1：使用 0.5 次方计算平方根
print(8 ** 0.5)  # 8的0.5次方 = 8的平方根 = √8 ≈ 2.8284271247461903

# 方法2：使用 1/2 次方计算平方根
print(8 ** (1 / 2))  # 同上，8的1/2次方 = 8的平方根 ≈ 2.8284271247461903

# 对于 √8:
# (√a)² = a
# (√8)² = 8

# 验证
result = (8 ** 0.5) ** 2
print(f"(√8)² = {result}")  # 输出: (√8)² = 8.0
print('-' * 50)

print(8 ** (1 / 3))  # 8的1/3次方 = 8的立方根 = ³√8 = 2
# 因为 2 × 2 × 2 = 8

print(pow(8, 1 / 3))  # 内置函数pow()，功能同上
# 8的立方根 = 2
print(pow(8, 1 / 2))  # 8 的平方根（√8）
print(pow(9, 2))  # 9的2次方 = 9 × 9 = 81
