# ============================================================
# 复合赋值运算符（增强赋值运算符）示例
# ============================================================

x = 10

# 加法赋值：等价于 x = x + 1
x += 1
print(x)  # 输出: 11

# 减法赋值：等价于 x = x - 1
x -= 1
print(x)  # 输出: 10

# 乘法赋值：等价于 x = x * 1
x *= 1
print(x)  # 输出: 10

# 除法赋值：等价于 x = x / 1（结果为浮点数）
x /= 1
print(x)  # 输出: 10.0

# 整除赋值：等价于 x = x // 3（向下取整除法）
x //= 3
print(x)  # 输出: 5.0（因为 x 是浮点数 10.0 // 3 = 3.0）

# 取模赋值：等价于 x = x % 2（求余数）
x %= 2
print(x)  # 输出: 1.0（5.0 ÷ 2 = 2 余 1.0）

# 重新赋值一个整数
y = 2

# 幂赋值：等价于 y = y ** 5（y 的 5 次方 = 2 × 2 × 2 × 2 × 2）
y **= 5
print(y)  # 输出: 32（因为 2⁵ = 32）




